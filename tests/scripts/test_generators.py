class TestGenerateClaude:
    def test_skips_non_claude_skills(self, tmp_path, minimal_skill):
        minimal_skill.applies_to = ["cursor"]
        generate_claude([minimal_skill], tmp_path)
        assert not list(tmp_path.rglob("*.json"))

    def test_generates_file(self, tmp_path, minimal_skill):
        minimal_skill.applies_to = ["claude"]
        generate_claude([minimal_skill], tmp_path)
        path = tmp_path / ".claude" / "skills" / "minimal.json"
        assert path.exists()
        data = json.loads(path.read_text())
        assert data["name"] == "minimal"


